_RPG_TTS_REPLACEMENTS = tuple(
    sorted(
        _RPG_TTS_TERM_REPLACEMENTS.items(),
        key=lambda item: len(item[0]),
        reverse=True,
    )
)


def normalize_rpg_tts_text(text: str) -> str:
    normalized = nfkc(str(text or "")).strip()
    if not normalized:
        return ""

    for source, replacement in _RPG_TTS_REPLACEMENTS:
        normalized = normalized.replace(source, replacement)
    return normalized


